package c322.homework.homework6.parta;

public class Profile {
    private String id;
    private String email;

    private String friends;


    public Profile(String id, String email) {
        this.id = id;
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public String getEmail() {
        return email;
    }

    public String getCoworkers() {
        return getCoworkers();
    }
    public String getProfiles() {
        return getProfiles();
    }

    public String getFriends() {
        return friends;
    }
}
